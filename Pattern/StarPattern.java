package Pattern;

public class StarPattern {
    public static void Star_Pattern(int n){
        for(int i=1;i<=n;i++){
            for(int j=1;j<=i;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
    public static void main(String[] args) {
        Star_Pattern(4);
    }
}
